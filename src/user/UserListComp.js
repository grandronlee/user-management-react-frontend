import * as React from 'react';
import { DataGrid } from '@material-ui/data-grid';

const columns = [
  { field: 'id', headerName: 'ID', width: 70 },
  { field: 'name', headerName: 'Name', width: 130 },
  { field: 'email', headerName: 'Email', width: 320 },
];

const USER_ENDPOINT = process.env.REACT_APP_USER_ENDPOINT

class UserListComp extends React.Component {
    constructor(props) {
      super(props);
      this.state = {
        error: null,
        isLoaded: false,
        items: []
      };
    }
  
    componentDidMount() {
      fetch(USER_ENDPOINT, 
          { 
            mode: "cors", 
            headers: {"Access-Control-Allow-Origin":"*"}
          }
        )
        .then(res => res.json())
        .then(
          (result) => {
            this.setState({
              isLoaded: true,
              items: result
            });
          },
          // Note: it's important to handle errors here
          // instead of a catch() block so that we don't swallow
          // exceptions from actual bugs in components.
          (error) => {
            this.setState({
              isLoaded: true,
              error
            });
          }
        )
    }
  
    render() {
      const { error, isLoaded, items } = this.state;
      if (error) {
        return <div>Error: {error.message}</div>;
      } else if (!isLoaded) {
        return <div>Loading...</div>;
      } else {
        return (
          <div style={{ height: 400, width: '98%' }}>
            <DataGrid rows={items} columns={columns} pageSize={5} checkboxSelection />
          </div>
        );
      }
    }
  }

  export default UserListComp;